// Función para generar los slides de consejos
function generarSlides() {
    var swiperWrapper = document.querySelector('.swiper-wrapper');
  
    consejos.consejos.forEach(function(consejo) {
      var slide = document.createElement('div');
      slide.classList.add('swiper-slide');
  
      var slideContent = document.createElement('div');
      slideContent.classList.add('slide-content');
  
      var titulo = document.createElement('h2');
      titulo.textContent = consejo.titulo;
  
      var descripcion = document.createElement('p');
      descripcion.textContent = consejo.descripcion;
  
      slideContent.appendChild(titulo);
      slideContent.appendChild(descripcion);
      slide.appendChild(slideContent);
      swiperWrapper.appendChild(slide);
    });
  
    // Inicializar el slider después de generar los slides
    var swiper = new Swiper('.swiper-container', {
      autoplay: {
        delay: 12000, // Tiempo de transición entre los slides (en milisegundos)
      },
      loop: true,
      speed: 2000, // Velocidad de movimiento del slider (en milisegundos)

    });
  }
  
  // Esperar a que el DOM esté cargado
  document.addEventListener("DOMContentLoaded", function() {
    // Llamar a la función para generar los slides
    generarSlides();
  });